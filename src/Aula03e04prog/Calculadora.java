package Aula03e04prog;

public class Calculadora {

	public void somar(int n1, int n2){
		
		int sum = n1 + n2; //soma os inteiros, depois armazena o total em sum
		
		System.out.printf("Soma igual a %d\n", sum); //exibe a soma
		
	} //fim do método somar
	
	public void subtrair(int n1, int n2){
		
		int sum = n1 - n2; //soma os inteiros, depois armazena o total em sum
		
		System.out.printf("Subtracao igual a %d\n", sum); //exibe a soma
		
	} //fim do método subtrair

} //fim da classe Calculadora