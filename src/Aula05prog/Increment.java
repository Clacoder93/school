package Aula05prog;

public class Increment {

	public static void main (String[] args) {
		int c;
		
		//Demonstra o operador de pos-incremento
		c=5;
		System.out.println( c );
		System.out.println( c++ );
		System.out.println( c );
		
		System.out.println( );
		
		//Demonstra o operador de pre-incremento
		c=5;
		System.out.println( c );
		System.out.println( ++c );
		System.out.println( c );
	}

}

